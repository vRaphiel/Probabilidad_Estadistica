par(mfrow=c(2,2))
b10 <- rbinom(1000, 10, 0.5)
hist(b10, freq = FALSE)
b100 <- rbinom(1000, 100, 0.05)
hist(b100, freq = FALSE)
b1000 <- rbinom(1000, 1000, 0.005)
hist(b1000, freq = FALSE)
pois <- rpois(1000, lambda = 5)
hist(pois, freq = FALSE)

